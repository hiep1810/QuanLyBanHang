    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed

        String tuKhoa = tfTimKiem.getText();
        if (cbTimKiem.getSelectedIndex() == 0) {
            NhaPP npp = nhaPPDao.timTheoMa(Integer.parseInt(tuKhoa));
            if (npp != null) {
                dtm.setRowCount(0);
                dtm.addRow(new String[]{npp.getMaNPP() + "", npp.getTenNPP(), npp.getSdt(), npp.getDiaChi()});
            }
        } else {
            List<NhaPP> li = nhaPPDao.timTheoTen(tuKhoa);
            if (li != null) {
                dtm.setRowCount(0);
                for (NhaPP nhaPP : li) {
                    dtm.addRow(new String[]{nhaPP.getMaNPP() + "", nhaPP.getTenNPP(), nhaPP.getSdt(), nhaPP.getDiaChi()});
                }
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

