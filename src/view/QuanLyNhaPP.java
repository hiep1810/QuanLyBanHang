    private void hienThiDS() {
        List<NhaPP> all = nhaPPDao.getAll();
        if (all != null) {
            dtm.setRowCount(0);
            for (NhaPP npp : all) {
                dtm.addRow(new String[]{npp.getMaNPP() + "", npp.getTenNPP(), npp.getSdt(), npp.getDiaChi()});
            }
        }
    }

    private void xoaText() {
        tfdiachi.setText("");
        tfsdt.setText("");
        tftennpp.setText("");
        tfTimKiem.setText("");
    }
